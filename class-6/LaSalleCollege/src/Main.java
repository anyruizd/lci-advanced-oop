public class Main {
    public static void main(String[] args) {
        // int is not a data type. is a field of the wrapper class Integer
        int a = 5;
        String name = "John";
        Integer b = new Integer(56);
        System.out.println("Hello world!");
        // Naming convention in java:
        /*
        * Class: always pascal case
        * Interface: pascal case
        * Methods: camel case
        * Constants: uppercase separated with uderscore for several words
        * */
    }
}